public class GameEntity {
    private int hpBoss;
    private int damageBoss;
    private String defenceBoss;
    private int hpHeroes;
    private int damageHeros;
    private String defenceHeroes;

    public int getHpBoss() {
        return hpBoss;
    }

    public void setHpBoss(int hpBoss) {
        this.hpBoss = hpBoss;
    }

    public int getDamageBoss() {
        return damageBoss;
    }

    public void setDamageBoss(int damageBoss) {
        this.damageBoss = damageBoss;
    }

    public String getDefenceBoss() {
        return defenceBoss;
    }

    public void setDefenceBoss(String defenceBoss) {
        this.defenceBoss = defenceBoss;
    }

    public int getHpHeroes() {
        return hpHeroes;
    }

    public void setHpHeroes(int hpHeroes) {
        this.hpHeroes = hpHeroes;
    }

    public int getDamageHeros() {
        return damageHeros;
    }

    public void setDamageHeros(int damageHeros) {
        this.damageHeros = damageHeros;
    }

    public String getDefenceHeroes() {
        return defenceHeroes;
    }

    public void setDefenceHeroes(String defenceHeroes) {
        this.defenceHeroes = defenceHeroes;
    }
}
